// （1）定义
// “A scientific hypothesis about the behaviour of the system under study”（关于所研究系统行为的科学假设）。
// （2）建模目的
// 科学理解
// 预测、仿真
// 管理 / 运营规划
// 控制系统设计

// 2. 模型类型及分类
// 模型可通过树状图分为四大维度，具体如下：
// 分类维度	            子类型 1	                    子类型 2
// 参数分布	集总参数模型（Lumped Parameter）	分布参数模型（Distributed Parameter）
// 线性特性	线性模型（Linear Model）	        非线性模型（Nonlinear Model）
// 建模依据	机理模型（Mechanistic Model）	    黑箱模型（Black Box Model）
// 时间维度	连续时间模型（Continuous-Time）	    离散时间模型（Discrete-Time）

// （1）参数分布维度
// 分布参数模型
// 核心：用偏微分方程描述变量在时间和空间所有点的变化规律。
// 实例：计算流体动力学（CFD）模型，如通风系统中气流速度（大小 + 方向）在 3D 空间的时间变化。
// 集总参数模型
// 核心：用常微分方程描述变量在固定空间点的时间变化规律。
// 实例：传递函数模型，如通风系统进出口气流的关系。

// （2）线性特性维度
// 线性模型
// 核心特性：服从叠加原理。
// 稳定性：稳定性是系统固有属性，与外部输入的性质和幅值无关。
// 非线性模型
// 核心特性：不服从叠加原理，对不同幅值但形状相似的输入响应差异显著。
// 行为：可能表现出复杂甚至混沌行为。
// 实例：洛伦兹吸引子（Lorenz attractor），对应方程：
// \(\frac{dx}{dy}=\sigma(y-x),\quad \frac{dy}{dt}=x(p-z)-y,\quad \frac{dz}{dy}=xy-\beta z\)
// 附加概念：吸引子（attractor）是系统从多种初始条件出发均趋向的数值集合，轻微扰动后仍保持接近。

// （3）建模依据维度
// 黑箱模型
// 核心：仅关联输入与输出变量，不考虑系统内部机制。
// 参数估计：通过图形或统计方法从数据中估计参数。
// 机理模型
// 核心：基于已知物理定律构建。
// 实例：用牛顿运动定律推导车辆速度模型，标准形式为\(\tau \dot{x}+x=Ku(t)\)。

// （4）时间维度
// 连续时间模型
// 适用系统：变量连续变化的 “模拟系统”。
// 表达形式：微分方程。
// 离散时间模型
// 适用系统：变量在离散时间间隔变化的 “数字系统”。
// 表达形式：差分方程。
// 关联：连续时间系统常通过数字计算机定期采样输出进行控制。

// 3. 典型模型实例
// （1）河流流量动态模型
// 输入\(u(t)\)：有效降雨量；输出\(y(t)\)：河流流量。
// 模型表达式：\(y(t)=\frac{0.01706 s^{2}+0.1578 s+0.001680}{s^{2}+0.3299 s+0.001768} u(t)\)（s为微分算子\(d/dt\)）。
// 模型来源：通过小时流量和有效降雨量数据统计估计得到。分类：线性、集总参数、黑箱、连续时间模型。

// （2）数据基机理（DBM）模型核心：结合数据（识别结构 + 估计参数）与物理意义（确保可解释性）。
// 实例：河流流量模型经部分分式展开为并行路径形式：
// \(y(t)=\left\{0.0171+\frac{0.1496}{s+0.3244}+\frac{0.002573}{s+0.00545}\right\} u(t)\)
// 物理解释：瞬时效应（1 小时内影响河流流量的少量有效降雨）、“快速流” 效应（地表及近地表过程）、“慢速流” 效应（地下水过程）。

// （3）车辆速度模型建模依据：牛顿第二定律（力 = 质量 × 加速度）。
// 假设条件：车辆质量为m，发动机力为\(u(t)\)，摩擦力与速度成正比（系数b，摩擦力bv），忽略车轮转动惯量。
// 推导过程：以位移y为输出：\(m \ddot{y}+b \dot{y}=u(t)\)（二阶微分方程）。以速度\(v=\dot{y}\)为输出：\(m \dot{v}+b v=u(t)\)（一阶微分方程）。
// 与一阶系统标准形式对应：标准形式：\(\tau \dot{x}+x=Ku(t)\)（\(\tau\)为时间常数，K为稳态增益）。
// 车辆模型对应：\(\tau=\frac{m}{b}\)，\(K=\frac{1}{b}\)。
// 阶跃响应：发动机力从 0 阶跃至 100%（满功率）时，速度响应可通过 Matlab/Simulink 仿真求解微分方程得到。

